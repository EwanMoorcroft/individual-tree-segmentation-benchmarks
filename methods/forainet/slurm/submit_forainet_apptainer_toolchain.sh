#!/usr/bin/env bash

set -euo pipefail

: "${FORAINET_BENCHMARK_ROOT:?set FORAINET_BENCHMARK_ROOT}"
: "${FORAINET_TOOLCHAIN_ROOT:?set FORAINET_TOOLCHAIN_ROOT}"
: "${FORAINET_TOOLCHAIN_EVIDENCE_ROOT:?set FORAINET_TOOLCHAIN_EVIDENCE_ROOT}"
: "${FORAINET_TOOLCHAIN_STATE_FILE:?set FORAINET_TOOLCHAIN_STATE_FILE}"
: "${FORAINET_EXPECTED_BENCHMARK_COMMIT:?set FORAINET_EXPECTED_BENCHMARK_COMMIT}"
: "${APPTAINER_CACHEDIR:?set APPTAINER_CACHEDIR}"

git -C "$FORAINET_BENCHMARK_ROOT" rev-parse --git-dir >/dev/null
test ! -e "$FORAINET_TOOLCHAIN_ROOT"
test ! -e "$FORAINET_TOOLCHAIN_EVIDENCE_ROOT"
test ! -e "$FORAINET_TOOLCHAIN_STATE_FILE"
test "$(git -C "$FORAINET_BENCHMARK_ROOT" rev-parse HEAD)" = \
  "$FORAINET_EXPECTED_BENCHMARK_COMMIT"
test -z "$(git -C "$FORAINET_BENCHMARK_ROOT" status --porcelain)"

mkdir -p \
  "$FORAINET_BENCHMARK_ROOT/logs" \
  "$(dirname "$FORAINET_TOOLCHAIN_STATE_FILE")"
job_id="$(
  cd "$FORAINET_BENCHMARK_ROOT"
  sbatch --parsable \
    --export=ALL,FORAINET_BENCHMARK_ROOT,FORAINET_TOOLCHAIN_ROOT,FORAINET_TOOLCHAIN_EVIDENCE_ROOT,FORAINET_EXPECTED_BENCHMARK_COMMIT,APPTAINER_CACHEDIR \
    methods/forainet/slurm/install_forainet_apptainer_toolchain.sbatch
)"
printf 'FORAINET_TOOLCHAIN_JOB_ID=%q\n' "$job_id" \
  > "$FORAINET_TOOLCHAIN_STATE_FILE"
printf 'FORAINET_TOOLCHAIN_ROOT=%q\n' "$FORAINET_TOOLCHAIN_ROOT" \
  >> "$FORAINET_TOOLCHAIN_STATE_FILE"
printf 'FORAINET_TOOLCHAIN_EVIDENCE_ROOT=%q\n' "$FORAINET_TOOLCHAIN_EVIDENCE_ROOT" \
  >> "$FORAINET_TOOLCHAIN_STATE_FILE"
printf '%s\n' "$job_id"
