#!/usr/bin/env bash

set -euo pipefail

: "${FORAINET_BENCHMARK_ROOT:?set FORAINET_BENCHMARK_ROOT}"
: "${FORAINET_EXPECTED_BENCHMARK_COMMIT:?set FORAINET_EXPECTED_BENCHMARK_COMMIT}"
: "${FORAINET_UPSTREAM_ROOT:?set FORAINET_UPSTREAM_ROOT}"
: "${FORAINET_IMAGE:?set FORAINET_IMAGE}"
: "${FORAINET_QUALIFICATION_ROOT:?set FORAINET_QUALIFICATION_ROOT}"
: "${FORAINET_DATASET_ROOT:?set FORAINET_DATASET_ROOT}"
: "${FORAINET_FINETUNE_ROOT:?set FORAINET_FINETUNE_ROOT}"
: "${FORAINET_FINETUNE_VALIDATION_ROOT:?set FORAINET_FINETUNE_VALIDATION_ROOT}"
: "${FORAINET_FINETUNE_VALIDATION_RUN_ID:?set FORAINET_FINETUNE_VALIDATION_RUN_ID}"
: "${FORAINET_FINETUNE_VALIDATION_STATE_FILE:?set FORAINET_FINETUNE_VALIDATION_STATE_FILE}"

if [[ "${FORAINET_FINETUNE_VALIDATION_CONFIRMED:-0}" != "1" ]]; then
  echo "Refusing fine-tune validation submission without confirmation."
  exit 2
fi
test "$(git -C "$FORAINET_BENCHMARK_ROOT" rev-parse HEAD)" = \
  "$FORAINET_EXPECTED_BENCHMARK_COMMIT"
test -z "$(git -C "$FORAINET_BENCHMARK_ROOT" status --porcelain)"
test -f "$FORAINET_FINETUNE_ROOT/full/final_gate.json"
test ! -e "$FORAINET_FINETUNE_VALIDATION_ROOT"
test ! -e "$FORAINET_FINETUNE_VALIDATION_STATE_FILE"

mkdir -p "$FORAINET_BENCHMARK_ROOT/logs" \
  "$(dirname "$FORAINET_FINETUNE_VALIDATION_ROOT")" \
  "$(dirname "$FORAINET_FINETUNE_VALIDATION_STATE_FILE")"
mkdir "$FORAINET_FINETUNE_VALIDATION_ROOT"
export_names="ALL,FORAINET_BENCHMARK_ROOT,FORAINET_EXPECTED_BENCHMARK_COMMIT,FORAINET_UPSTREAM_ROOT,FORAINET_IMAGE,FORAINET_QUALIFICATION_ROOT,FORAINET_DATASET_ROOT,FORAINET_FINETUNE_ROOT,FORAINET_FINETUNE_VALIDATION_ROOT,FORAINET_FINETUNE_VALIDATION_RUN_ID,FORAINET_FINETUNE_VALIDATION_CONFIRMED"
array_job="$(
  cd "$FORAINET_BENCHMARK_ROOT"
  sbatch --parsable \
    --array="0-24%2" \
    --export="$export_names" \
    methods/forainet/slurm/run_forainet_finetune_validation.sbatch
)"
summary_job="$(
  cd "$FORAINET_BENCHMARK_ROOT"
  sbatch --parsable \
    --dependency="afterany:$array_job" \
    --export="$export_names" \
    methods/forainet/slurm/summarise_forainet_finetune_validation.sbatch
)"
{
  printf 'FORAINET_FINETUNE_VALIDATION_ARRAY_JOB_ID=%q\n' "$array_job"
  printf 'FORAINET_FINETUNE_VALIDATION_SUMMARY_JOB_ID=%q\n' "$summary_job"
  printf 'FORAINET_FINETUNE_VALIDATION_RUN_ID=%q\n' \
    "$FORAINET_FINETUNE_VALIDATION_RUN_ID"
  printf 'FORAINET_FINETUNE_VALIDATION_ROOT=%q\n' \
    "$FORAINET_FINETUNE_VALIDATION_ROOT"
  printf 'FORAINET_EXPECTED_BENCHMARK_COMMIT=%q\n' \
    "$FORAINET_EXPECTED_BENCHMARK_COMMIT"
  printf 'FORAINET_SUBMITTED_AT_EPOCH=%q\n' "$(date +%s)"
} > "$FORAINET_FINETUNE_VALIDATION_STATE_FILE"
printf 'validation_array_job=%s summary_job=%s\n' "$array_job" "$summary_job"
