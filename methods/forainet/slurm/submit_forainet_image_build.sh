#!/usr/bin/env bash

set -euo pipefail

: "${FORAINET_BENCHMARK_ROOT:?set FORAINET_BENCHMARK_ROOT}"
: "${FORAINET_DEFINITION:?set FORAINET_DEFINITION}"
: "${FORAINET_IMAGE:?set FORAINET_IMAGE}"
: "${FORAINET_IMAGE_BUILD_ROOT:?set FORAINET_IMAGE_BUILD_ROOT}"
: "${FORAINET_EXPECTED_BENCHMARK_COMMIT:?set FORAINET_EXPECTED_BENCHMARK_COMMIT}"
: "${FORAINET_IMAGE_BUILD_STATE_FILE:?set FORAINET_IMAGE_BUILD_STATE_FILE}"
: "${APPTAINER_CACHEDIR:?set APPTAINER_CACHEDIR}"
: "${APPTAINER_TMPDIR:?set APPTAINER_TMPDIR}"

git -C "$FORAINET_BENCHMARK_ROOT" rev-parse --git-dir >/dev/null
test -f "$FORAINET_DEFINITION"
test ! -e "$FORAINET_IMAGE"
test ! -e "$FORAINET_IMAGE_BUILD_ROOT"
test ! -e "$FORAINET_IMAGE_BUILD_STATE_FILE"
test "$(git -C "$FORAINET_BENCHMARK_ROOT" rev-parse HEAD)" = \
  "$FORAINET_EXPECTED_BENCHMARK_COMMIT"
test -z "$(git -C "$FORAINET_BENCHMARK_ROOT" status --porcelain)"

mkdir -p \
  "$FORAINET_BENCHMARK_ROOT/logs" \
  "$(dirname "$FORAINET_IMAGE_BUILD_STATE_FILE")"
job_id="$(
  cd "$FORAINET_BENCHMARK_ROOT"
  sbatch --parsable \
    --export=ALL,FORAINET_BENCHMARK_ROOT,FORAINET_DEFINITION,FORAINET_IMAGE,FORAINET_IMAGE_BUILD_ROOT,FORAINET_EXPECTED_BENCHMARK_COMMIT,APPTAINER_CACHEDIR,APPTAINER_TMPDIR \
    methods/forainet/slurm/build_forainet_image.sbatch
)"
printf 'FORAINET_IMAGE_BUILD_JOB_ID=%q\n' "$job_id" \
  > "$FORAINET_IMAGE_BUILD_STATE_FILE"
printf 'FORAINET_IMAGE_BUILD_ROOT=%q\n' "$FORAINET_IMAGE_BUILD_ROOT" \
  >> "$FORAINET_IMAGE_BUILD_STATE_FILE"
printf 'FORAINET_IMAGE=%q\n' "$FORAINET_IMAGE" \
  >> "$FORAINET_IMAGE_BUILD_STATE_FILE"
printf '%s\n' "$job_id"
