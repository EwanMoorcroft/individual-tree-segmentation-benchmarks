#!/usr/bin/env bash

set -euo pipefail

: "${FORAINET_BENCHMARK_ROOT:?set FORAINET_BENCHMARK_ROOT}"
: "${FORAINET_UPSTREAM_ROOT:?set FORAINET_UPSTREAM_ROOT}"
: "${FORAINET_CHECKPOINT:?set FORAINET_CHECKPOINT}"
: "${FORAINET_IMAGE:?set FORAINET_IMAGE}"
: "${FORAINET_QUALIFICATION_ROOT:?set FORAINET_QUALIFICATION_ROOT}"
: "${FORAINET_EXPECTED_BENCHMARK_COMMIT:?set FORAINET_EXPECTED_BENCHMARK_COMMIT}"
: "${FORAINET_STATE_FILE:?set FORAINET_STATE_FILE}"

test ! -e "$FORAINET_QUALIFICATION_ROOT"
test ! -e "$FORAINET_STATE_FILE"
test -f "$FORAINET_IMAGE"
test -f "$FORAINET_CHECKPOINT"
test -d "$FORAINET_UPSTREAM_ROOT/.git"
test "$(git -C "$FORAINET_BENCHMARK_ROOT" rev-parse HEAD)" = \
  "$FORAINET_EXPECTED_BENCHMARK_COMMIT"
test -z "$(git -C "$FORAINET_BENCHMARK_ROOT" status --porcelain)"

mkdir -p "$FORAINET_BENCHMARK_ROOT/logs" "$(dirname "$FORAINET_STATE_FILE")"
job_id="$(
  cd "$FORAINET_BENCHMARK_ROOT"
  sbatch --parsable \
    --export=ALL,FORAINET_BENCHMARK_ROOT,FORAINET_UPSTREAM_ROOT,FORAINET_CHECKPOINT,FORAINET_IMAGE,FORAINET_QUALIFICATION_ROOT,FORAINET_EXPECTED_BENCHMARK_COMMIT \
    methods/forainet/slurm/qualify_forainet_assets.sbatch
)"
printf 'FORAINET_QUALIFICATION_JOB_ID=%q\n' "$job_id" > "$FORAINET_STATE_FILE"
printf 'FORAINET_QUALIFICATION_ROOT=%q\n' "$FORAINET_QUALIFICATION_ROOT" >> "$FORAINET_STATE_FILE"
printf '%s\n' "$job_id"
