#!/usr/bin/env bash

set -euo pipefail

if [[ "${TLS2TREES_PUBLISHED_DEFAULT_RESULTS_CONFIRMED:-0}" != "1" ]]; then
  echo "Refusing published-default result finalisation." >&2
  echo "Set TLS2TREES_PUBLISHED_DEFAULT_RESULTS_CONFIRMED=1 after reviewing the completed test." >&2
  exit 2
fi
REVIEWED_SHA256="${TLS2TREES_REVIEWED_PUBLISHED_DEFAULT_CONFIG_SHA256:?Set the reviewed published-default config SHA-256}"
STATE_FILE="${1:?Usage: finalise_published_default_results.sh <completed-state-file>}"
PROJECT_ROOT="${TLS2TREES_PROJECT_ROOT:-$HOME/scratch/tree-seg-benchmark}"

cd "$PROJECT_ROOT"
test -d .git
test -z "$(git status --porcelain)"
test -f "$STATE_FILE"
# shellcheck disable=SC1090
source "$STATE_FILE"

test "$TLS2TREES_PD_TEST_SUBMISSION_STATUS" = "published_default_test_chain_submitted"
test "$TLS2TREES_PD_TEST_CONFIGURATION_CHANGED_AFTER_TEST" = "false"
test "$REVIEWED_SHA256" = "$TLS2TREES_PD_TEST_PUBLISHED_CONFIG_SHA256"
test "$(sha256sum "$TLS2TREES_PD_TEST_PUBLISHED_CONFIG" | awk '{print $1}')" = "$REVIEWED_SHA256"
git cat-file -e "${TLS2TREES_PD_TEST_BENCHMARK_COMMIT}^{commit}"
git merge-base --is-ancestor \
  "$TLS2TREES_PD_TEST_BENCHMARK_COMMIT" "$(git rev-parse HEAD)"
test "$TLS2TREES_PD_TEST_UPSTREAM_COMMIT" = "ca12cb73b2c736d80b020e8025f8d975d42e6f01"
test "$TLS2TREES_PD_TEST_MODEL_SHA256" = "1a8bb6372394600f7c4b15f76beb98c32cb47ed25f8f729a84117ccfa410e72b"

SUMMARY_STATE=$(
  sacct -X -n -P -j "$TLS2TREES_PD_TEST_SUMMARY_JOB" \
    --format=JobIDRaw,State |
    awk -F'|' -v id="$TLS2TREES_PD_TEST_SUMMARY_JOB" \
      '$1 == id {print $2; exit}'
)
SUMMARY_STATE="${SUMMARY_STATE%%+*}"
SUMMARY_STATE="${SUMMARY_STATE%% *}"
if [[ "$SUMMARY_STATE" != "COMPLETED" ]]; then
  echo "Refusing: summary job $TLS2TREES_PD_TEST_SUMMARY_JOB is ${SUMMARY_STATE:-unknown}." >&2
  exit 2
fi

for path in \
  "$TLS2TREES_PD_TEST_MANIFEST_JSON" \
  "$TLS2TREES_PD_TEST_MANIFEST_SHA256_FILE" \
  "$TLS2TREES_PD_TEST_SUMMARY_JSON" \
  "$TLS2TREES_PD_TEST_PLOT_CSV" \
  "$TLS2TREES_PD_TEST_AGGREGATE_CSV" \
  "$TLS2TREES_PD_TEST_RETENTION_JSON"; do
  test -s "$path"
done

RECEIPT_JSON="$(dirname "$TLS2TREES_PD_TEST_SUMMARY_JSON")/published_default_publication_receipt.json"
EXPORTS="ALL,TLS2TREES_PUBLISHED_DEFAULT_RESULTS_CONFIRMED=1,TLS2TREES_PD_FINALISE_PROJECT_ROOT=$PROJECT_ROOT,TLS2TREES_PD_FINALISE_TREEBENCH_ENV=$TLS2TREES_PD_TEST_TREEBENCH_ENV,TLS2TREES_PD_FINALISE_RUN_ID=$TLS2TREES_PD_TEST_RUN_ID,TLS2TREES_PD_FINALISE_BENCHMARK_COMMIT=$TLS2TREES_PD_TEST_BENCHMARK_COMMIT,TLS2TREES_PD_FINALISE_UPSTREAM_COMMIT=$TLS2TREES_PD_TEST_UPSTREAM_COMMIT,TLS2TREES_PD_FINALISE_MODEL_SHA256=$TLS2TREES_PD_TEST_MODEL_SHA256,TLS2TREES_PD_FINALISE_WORKFLOW_CONFIG=$TLS2TREES_PD_TEST_WORKFLOW_CONFIG,TLS2TREES_PD_FINALISE_WORKFLOW_CONFIG_SHA256=$TLS2TREES_PD_TEST_WORKFLOW_CONFIG_SHA256,TLS2TREES_PD_FINALISE_PUBLISHED_CONFIG=$TLS2TREES_PD_TEST_PUBLISHED_CONFIG,TLS2TREES_PD_FINALISE_PUBLISHED_CONFIG_SHA256=$TLS2TREES_PD_TEST_PUBLISHED_CONFIG_SHA256,TLS2TREES_PD_FINALISE_BENCHMARK_CONFIG=$TLS2TREES_PD_TEST_BENCHMARK_CONFIG,TLS2TREES_PD_FINALISE_BENCHMARK_CONFIG_SHA256=$TLS2TREES_PD_TEST_BENCHMARK_CONFIG_SHA256,TLS2TREES_PD_FINALISE_MANIFEST_JSON=$TLS2TREES_PD_TEST_MANIFEST_JSON,TLS2TREES_PD_FINALISE_MANIFEST_SHA256_FILE=$TLS2TREES_PD_TEST_MANIFEST_SHA256_FILE,TLS2TREES_PD_FINALISE_SUMMARY_JSON=$TLS2TREES_PD_TEST_SUMMARY_JSON,TLS2TREES_PD_FINALISE_PLOT_CSV=$TLS2TREES_PD_TEST_PLOT_CSV,TLS2TREES_PD_FINALISE_TARGET_CSV=$TLS2TREES_PD_TEST_AGGREGATE_CSV,TLS2TREES_PD_FINALISE_SOURCE_RETENTION_JSON=$TLS2TREES_PD_TEST_RETENTION_JSON,TLS2TREES_PD_FINALISE_RECEIPT_JSON=$RECEIPT_JSON"

JOB_ID=$(sbatch --parsable --export="$EXPORTS" \
  methods/tls2trees/slurm/for_instance/finalise_published_default_results.sbatch)
printf '%s\n' "$JOB_ID" > \
  logs/tls2trees_for_instance/latest_published_default_finalisation_job_id.txt

echo "status=published_default_finalisation_submitted"
echo "job_id=$JOB_ID"
echo "run_id=$TLS2TREES_PD_TEST_RUN_ID"
echo "reviewed_published_config_sha256=$REVIEWED_SHA256"
echo "receipt=$RECEIPT_JSON"
