#!/usr/bin/env bash

set -euo pipefail

if [[ "${TLS2TREES_FINAL_EVALUATION_SUMMARY_RESUME_CONFIRMED:-0}" != "1" ]]; then
  echo "Refusing TLS2trees retained-evaluation summary resume." >&2
  echo "Set TLS2TREES_FINAL_EVALUATION_SUMMARY_RESUME_CONFIRMED=1 after checking the array and summary job." >&2
  exit 2
fi

PROJECT_ROOT="${TLS2TREES_PROJECT_ROOT:-$HOME/scratch/tree-seg-benchmark}"
STATE_FILE="${1:-}"
if [[ -z "$STATE_FILE" ]]; then
  POINTER="$PROJECT_ROOT/logs/tls2trees_for_instance/latest_final_evaluation_state_file.txt"
  test -s "$POINTER"
  STATE_FILE=$(tr -d '\r\n' < "$POINTER")
fi
test -f "$STATE_FILE"
# shellcheck disable=SC1090
source "$STATE_FILE"

cd "$PROJECT_ROOT"
test -d .git
test -z "$(git status --porcelain)"
SUMMARY_BENCHMARK_COMMIT=$(git rev-parse HEAD)
test -x "$TLS2TREES_FINAL_EVALUATION_TREEBENCH_ENV/bin/python"
test -f "$TLS2TREES_FINAL_EVALUATION_PLAN_JSON"
test -f "$TLS2TREES_FINAL_EVALUATION_CLI"
test -f "$TLS2TREES_FINAL_EVALUATION_EVALUATOR"
test "$(sha256sum "$TLS2TREES_FINAL_EVALUATION_PLAN_JSON" | awk '{print $1}')" = \
  "$TLS2TREES_FINAL_EVALUATION_PLAN_SHA256"
test "$(sha256sum "$TLS2TREES_FINAL_EVALUATION_EVALUATOR" | awk '{print $1}')" = \
  "$TLS2TREES_FINAL_EVALUATION_EVALUATOR_SHA256"

COMPLETED_TASKS=$(sacct -X -n -P -j "$TLS2TREES_FINAL_EVALUATION_JOB" \
  --format=JobID%30,State | awk -F'|' -v id="$TLS2TREES_FINAL_EVALUATION_JOB" '
    {gsub(/^[[:space:]]+|[[:space:]]+$/, "", $1)}
    $1 ~ ("^" id "_[0-9]+$") {
      sub(/[+ ].*$/, "", $2)
      if ($2 == "COMPLETED") count++
    }
    END {print count+0}')
if [[ "$COMPLETED_TASKS" != "106" ]]; then
  echo "Expected 106 completed evaluation tasks; sacct reported $COMPLETED_TASKS." >&2
  exit 2
fi

SOURCE_SUMMARY_STATE=$(sacct -X -n -P \
  -j "$TLS2TREES_FINAL_EVALUATION_SUMMARY_JOB" \
  --format=JobIDRaw,State | awk -F'|' \
  -v id="$TLS2TREES_FINAL_EVALUATION_SUMMARY_JOB" '
    $1 == id {sub(/[+ ].*$/, "", $2); print $2; exit}')
case "$SOURCE_SUMMARY_STATE" in
  COMPLETED|FAILED|CANCELLED|TIMEOUT) ;;
  *)
    echo "Summary job is not terminal: ${SOURCE_SUMMARY_STATE:-unknown}" >&2
    exit 2
    ;;
esac

for path in "$TLS2TREES_FINAL_EVALUATION_PLOT_CSV" \
  "$TLS2TREES_FINAL_EVALUATION_AGGREGATE_CSV" \
  "$TLS2TREES_FINAL_EVALUATION_TEST_SUMMARY_JSON" \
  "$TLS2TREES_FINAL_EVALUATION_TEST_PLOT_CSV" \
  "$TLS2TREES_FINAL_EVALUATION_TEST_AGGREGATE_CSV"; do
  mkdir -p "$(dirname "$path")"
done

CLI_SHA256=$(sha256sum "$TLS2TREES_FINAL_EVALUATION_CLI" | awk '{print $1}')
COMMON_EXPORTS="ALL,TLS2TREES_FINAL_EVALUATION_CONFIRMED=1,TLS2TREES_FINAL_EVALUATION_RESUME_EXACT_PARTIAL=1,TLS2TREES_PROJECT_ROOT=$PROJECT_ROOT,TLS2TREES_FINAL_EVALUATION_SUMMARY_BENCHMARK_COMMIT=$SUMMARY_BENCHMARK_COMMIT,TLS2TREES_FINAL_EVALUATION_PLAN_JSON=$TLS2TREES_FINAL_EVALUATION_PLAN_JSON,TLS2TREES_FINAL_EVALUATION_PLAN_SHA256=$TLS2TREES_FINAL_EVALUATION_PLAN_SHA256,TLS2TREES_FINAL_EVALUATION_CLI=$TLS2TREES_FINAL_EVALUATION_CLI,TLS2TREES_FINAL_EVALUATION_CLI_SHA256=$CLI_SHA256,TLS2TREES_FINAL_EVALUATION_EVALUATOR=$TLS2TREES_FINAL_EVALUATION_EVALUATOR,TLS2TREES_FINAL_EVALUATION_EVALUATOR_SHA256=$TLS2TREES_FINAL_EVALUATION_EVALUATOR_SHA256,TLS2TREES_FINAL_EVALUATION_TREEBENCH_ENV=$TLS2TREES_FINAL_EVALUATION_TREEBENCH_ENV,TLS2TREES_FINAL_EVALUATION_SUMMARY_JSON=$TLS2TREES_FINAL_EVALUATION_SUMMARY_JSON,TLS2TREES_FINAL_EVALUATION_PLOT_CSV=$TLS2TREES_FINAL_EVALUATION_PLOT_CSV,TLS2TREES_FINAL_EVALUATION_AGGREGATE_CSV=$TLS2TREES_FINAL_EVALUATION_AGGREGATE_CSV,TLS2TREES_FINAL_EVALUATION_TEST_SUMMARY_JSON=$TLS2TREES_FINAL_EVALUATION_TEST_SUMMARY_JSON,TLS2TREES_FINAL_EVALUATION_TEST_PLOT_CSV=$TLS2TREES_FINAL_EVALUATION_TEST_PLOT_CSV,TLS2TREES_FINAL_EVALUATION_TEST_AGGREGATE_CSV=$TLS2TREES_FINAL_EVALUATION_TEST_AGGREGATE_CSV"

SUMMARY_JOB=$(sbatch --parsable --export="$COMMON_EXPORTS" \
  methods/tls2trees/slurm/for_instance/summarise_retained_predictions.sbatch)

STAMP=$(date -u +%Y%m%d_%H%M%S)
RESUME_STATE="${STATE_FILE%.env}_summary_resume_$STAMP.env"
test ! -e "$RESUME_STATE"
{
  cat "$STATE_FILE"
  printf 'TLS2TREES_FINAL_EVALUATION_SUBMISSION_STATUS=%q\n' \
    "exact_summary_resume_submitted"
  printf 'TLS2TREES_FINAL_EVALUATION_SUMMARY_JOB=%q\n' "$SUMMARY_JOB"
  printf 'TLS2TREES_FINAL_EVALUATION_SUMMARY_BENCHMARK_COMMIT=%q\n' \
    "$SUMMARY_BENCHMARK_COMMIT"
  printf 'TLS2TREES_FINAL_EVALUATION_CLI_SHA256=%q\n' "$CLI_SHA256"
  printf 'TLS2TREES_FINAL_EVALUATION_RESUME_FROM_SUMMARY_JOB=%q\n' \
    "$TLS2TREES_FINAL_EVALUATION_SUMMARY_JOB"
} > "$RESUME_STATE"
printf '%s\n' "$RESUME_STATE" > \
  logs/tls2trees_for_instance/latest_final_evaluation_state_file.txt

echo "status=tls2trees_exact_summary_resume_submitted"
echo "source_summary_job=$TLS2TREES_FINAL_EVALUATION_SUMMARY_JOB"
echo "summary_job=$SUMMARY_JOB"
echo "state_file=$RESUME_STATE"
echo "evaluation_tasks_rerun=false"
echo "existing_outputs_exactly_verified=true"
